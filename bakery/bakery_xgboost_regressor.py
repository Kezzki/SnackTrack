"""# Stock recommendation

The "Safety Stock" Approach

Because the model has an RMSE of 0.9095, there is a statistical "error margin." To avoid running out of stock (Stockout), we apply a Ceiling Function to the upper bound of the prediction.

Baseline Prediction: $1.4$ units (The mathematical average).

Rounding: $1$ unit (50% chance of a stockout).

Safety Recommendation: $\lceil 1.4 + \text{RMSE} \rceil = \lceil
2.3095 \rceil = 3$ units.
"""

