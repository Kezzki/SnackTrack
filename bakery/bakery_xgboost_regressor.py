# --- HELPER: iterative forecast that seeds lag features day-by-day ---
def iterative_forecast(model, features, article_id, avg_price, start_date, num_days, history_df, article_name):
    """
    Forecast `num_days` into the future starting from `start_date`.
    Uses `history_df` (daily data for this article) to seed lag features,
    then walks forward one day at a time, feeding each prediction back
    as the next day's sales_1_day_ago.
    Returns a DataFrame with date + Predicted_Sales columns.
    """
    # Get the last known sales for this article (sorted by date)
    art_hist = history_df[history_df['article'] == article_name].sort_values('date_dt')
    recent_sales = list(art_hist['Quantity'].tail(7).values)  # last 7 days

    rows = []
    for i in range(num_days):
        d = start_date + pd.Timedelta(days=i)
        s1  = recent_sales[-1] if len(recent_sales) >= 1 else 0
        s7  = recent_sales[-7] if len(recent_sales) >= 7 else (recent_sales[0] if recent_sales else 0)
        r7  = float(np.mean(recent_sales[-7:])) if recent_sales else 0.0
        hol = int(d in fr_holidays)

        row = {
            'article_encoded': article_id,
            'unit_price':      avg_price,
            'day_of_week':     d.dayofweek,
            'is_weekend':      int(d.dayofweek >= 5),
            'month':           d.month,
            'sales_1_day_ago': s1,
            'sales_7_days_ago':s7,
            'rolling_7_day_avg': r7,
            'is_holiday':      hol,
        }
        pred_log = model.predict(pd.DataFrame([row])[features])[0]
        pred_qty = np.expm1(pred_log)
        pred_qty = max(pred_qty, 0)  # clamp negatives

        rows.append({'date': d, 'Predicted_Sales': pred_qty})
        recent_sales.append(pred_qty)  # feed prediction back as history

    return pd.DataFrame(rows)

